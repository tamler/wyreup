// One-off: read the workflow's per-batch seoContent files, validate, and emit
// packages/web/src/lib/seo-content.ts as a single typed data module.
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { createDefaultRegistry } from '../packages/core/dist/node/index.js';

// Repo-local scratch dir, written by seo-manifest.mjs (not shared /tmp).
const dir = fileURLToPath(new URL('../.cache/seo', import.meta.url));
const registry = createDefaultRegistry();
const validIds = new Set(Array.from(registry.toolsById.keys()));

const merged = {};
const problems = [];

for (const f of readdirSync(dir)
  .filter((n) => /^batch-\d+\.json$/.test(n))
  .sort()) {
  let arr;
  try {
    arr = JSON.parse(readFileSync(`${dir}/${f}`, 'utf8'));
  } catch (e) {
    problems.push(`${f}: unparseable (${e.message})`);
    continue;
  }
  if (!Array.isArray(arr)) {
    problems.push(`${f}: not an array`);
    continue;
  }
  for (const entry of arr) {
    const { id, seoContent: sc } = entry ?? {};
    if (!id || !validIds.has(id)) {
      problems.push(`${f}: bad id "${id}"`);
      continue;
    }
    if (
      !sc ||
      !sc.intro ||
      !Array.isArray(sc.useCases) ||
      sc.useCases.length < 3 ||
      !Array.isArray(sc.faq) ||
      sc.faq.length < 3
    ) {
      problems.push(`${id}: incomplete seoContent`);
      continue;
    }
    // Drop alsoTry links to unknown / self ids.
    if (Array.isArray(sc.alsoTry)) {
      sc.alsoTry = sc.alsoTry.filter((a) => a && validIds.has(a.id) && a.id !== id);
    }
    if (merged[id]) {
      problems.push(`${id}: duplicate (kept first)`);
      continue;
    }
    merged[id] = sc;
  }
}

const ids = Object.keys(merged).sort();
const body = ids
  .map(
    (id) =>
      `  ${JSON.stringify(id)}: ${JSON.stringify(merged[id], null, 2).split('\n').join('\n  ')},`,
  )
  .join('\n');

const out = `// AUTO-GENERATED by scripts/seo-assemble.mjs from the seo-backfill workflow.
// Per-tool SEO content (intro, common uses, FAQ, cross-links) merged onto tool
// pages via \`tool.seoContent ?? SEO_CONTENT[id]\`. Inline ToolModule.seoContent
// (the original 8 tools) takes precedence. Regenerate; do not hand-edit.
import type { ToolSeoContent } from '@wyreup/core';

export const SEO_CONTENT: Record<string, ToolSeoContent> = {
${body}
};
`;

writeFileSync('packages/web/src/lib/seo-content.ts', out);
console.log(`Assembled ${ids.length} tools into packages/web/src/lib/seo-content.ts`);
if (problems.length) {
  console.log(`\n${problems.length} problem(s):`);
  for (const p of problems.slice(0, 40)) console.log(`  - ${p}`);
}
